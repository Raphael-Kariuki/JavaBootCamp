public class MathFunction {
    public static void main(String[] args) {
        double x, y;
        x = 3;
        y = 4.7887;


        System.out.println(Math.pow(x, y));
        System.out.println(Math.min(x, y));
        System.out.println(Math.sqrt(y));
        System.out.println(Math.abs(y));
    }
}
